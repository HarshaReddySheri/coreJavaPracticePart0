package comSnoA2.harsha.Class;

public class rulesforvariable {
	static String name = "Harsha";
	static String NAME = "Reddy"; //use CASE SENSITIVE  and  _  $
	static String firstName = "sai"; //Camel casing ,avoid using keywords & short form
	static double MATH_PI = 3.14; //_use for constants VALUE write in capital and separate with underscore
	public static void main(String[] args) {
		System.out.print(name);
		System.out.println(NAME);
		System.out.println(firstName);
		System.out.println(MATH_PI);

	}

}