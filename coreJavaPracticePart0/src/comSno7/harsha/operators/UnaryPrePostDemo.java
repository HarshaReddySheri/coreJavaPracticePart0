package comSno7.harsha.operators;
public class UnaryPrePostDemo{
	public static void main(String args[]) {
	
	int i = 3;
	i++;
	//prints4
	System.out.println(i);
	
	++i;
	//prints 5
	System.out.println(i);
	
	//prints 6
	System.out.println(++i);
	
	//prints 6
	System.out.println(i++);
	
	//prints7
	System.out.println(i);
	
	}
	
	
}
